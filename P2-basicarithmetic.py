#Program that performs addition, subtraction, multiplication and division of two numbers

num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

print("addition: {}".format(num1 + num2))
print("subtraction: {}".format(num1 - num2))
print("multiplication: {}".format(num1 * num2))
print("division: {}".format(int(num1 / num2)))